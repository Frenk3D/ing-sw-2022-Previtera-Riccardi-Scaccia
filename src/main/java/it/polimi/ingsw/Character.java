package it.polimi.ingsw;

public class Character {
    private int numOfUse;
    private int id;
    private int initialCost;

    public int getNumOfUse() {
        return numOfUse;
    }

    public int getId() {
        return id;
    }
    public int getInitialCost(){
        return initialCost;
    }

    public void setNumOfUse(int numOfUse) {
        this.numOfUse = numOfUse;
    }

}
